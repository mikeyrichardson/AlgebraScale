package net.mvla.la.cs.as.model;

public class LinearExpressionFormatException extends Exception {
	public LinearExpressionFormatException() {
		super();
	}

	public LinearExpressionFormatException(String message) {
		super(message);
	}
	
	public LinearExpressionFormatException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public LinearExpressionFormatException(Throwable cause) {
		super(cause);
	}

}
